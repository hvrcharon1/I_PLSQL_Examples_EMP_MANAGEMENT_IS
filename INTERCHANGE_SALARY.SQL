SET SERVEROUTPUT ON 
DECLARE
    V_SALARY  EMPLOYEES.SALARY%TYPE;

BEGIN
   SELECT SALARY INTO V_SALARY FROM EMPLOYEES WHERE EMPLOYEE_ID = 120;
   
   UPDATE EMPLOYEES SET SALARY = ( SELECT SALARY FROM EMPLOYEES WHERE EMPLOYEES_ID = 130)
   WHERE EMPLOYEE_ID = 120;
   
   UPDATE EMPLOYEES SET SALARY = V_SALARY WHERE EMPLOYEE_ID = 130;
   
   COMMIT; 
   
  DBMS_OUTPUT.PUT_LINE('Interchanged successfully!');
   
END;   
   
   
   
