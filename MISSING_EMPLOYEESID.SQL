SET SERVEROUTPUT ON
DECLARE
   V_START EMPLOYEES.EMPLOYEE_ID%TYPE;
   V_END   EMPLOYEES.EMPLOYEE_ID%TYPE;
   V_COUNT NUMBER(1);
BEGIN

   SELECT MIN(EMPLOYEE_ID) + 1 ,  MAX(EMPLOYEE_ID) - 1 INTO V_START, V_END FROM EMPLOYEES;
   
   FRO EMPID IN V_START..V_END
   LOOP
      SELECT COUNT(*) INTO V_COUNT FROM EMPLOYEES WHERE EPLOYEE_ID = EMPID;
      
      IF V_COUNT = 0 THEN
        DBMS_OUTPUT.PUT_LINE(EMPID);
      END IF;
      
   END LOOP;
   
END;
